package com.ifly.travel.entity;

import java.util.Date;

public class Ticket {
	private int ticketid;
	private int spotid;
	private int price;
	private Date buydate;
	private int travelnum;
	private String passangername;
	public int getTicketid() {
		return ticketid;
	}
	public void setTicketid(int ticketid) {
		this.ticketid = ticketid;
	}
	public int getSpotid() {
		return spotid;
	}
	public void setSpotid(int spotid) {
		this.spotid = spotid;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public Ticket(int ticketid, int spotid, int price, Date buydate,
			int travelnum, String passangername) {
		super();
		this.ticketid = ticketid;
		this.spotid = spotid;
		this.price = price;
		this.buydate = buydate;
		this.travelnum = travelnum;
		this.passangername = passangername;
	}
	public Ticket(int ticketid, int spotid, int price, Date buydate) {
		super();
		this.ticketid = ticketid;
		this.spotid = spotid;
		this.price = price;
		this.buydate = buydate;
	}
	public Ticket(int ticketid, int spotid, int price) {
		super();
		this.ticketid = ticketid;
		this.spotid = spotid;
		this.price = price;
	}
	public Ticket(int spotid, int price) {
		super();
		this.spotid = spotid;
		this.price = price;
	}
	public Ticket() {
		super();
	}
	
	public Date getBuydate() {
		return buydate;
	}
	public void setBuydate(Date buydate) {
		this.buydate = buydate;
	}
	public String getPassangername() {
		return passangername;
	}
	public void setPassangername(String passangername) {
		this.passangername = passangername;
	}
	public int getTravelnum() {
		return travelnum;
	}
	public void setTravelnum(int travelnum) {
		this.travelnum = travelnum;
	}

}
